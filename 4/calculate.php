<?php

// Объявляем функцию evaluate, которая будет вычислять математическое выражение
function evaluate($expr) {

    // Удаляем все пробелы из строки, потому что они не нужны для вычислений
    $expr = preg_replace('/\s+/', '', $expr);

    // Если вся строка — это число (например "123" или "3.14")
    if (is_numeric($expr)) {
        // Превращаем строку в число с плавающей точкой и возвращаем
        return (float)$expr;
    }

    // Ищем в строке любую пару скобок, внутри которых нет других скобок
    while (preg_match('/\(([^()]+)\)/', $expr, $matches)) {
        // $matches[1] — это то, что было внутри скобок
        // Рекурсивно вызываем evaluate для этого внутреннего выражения
        $inner = evaluate($matches[1]);

        // Заменяем найденные скобки вместе с содержимым на результат их вычисления
        $expr = str_replace($matches[0], $inner, $expr);
    }

    // Ищем операции умножения и деления
    if (preg_match('/([+\-]?[0-9.]+)([\/\*])([+\-]?[0-9.]+)/', $expr, $m)) {

        // Превращаем найденные числа из строк в настоящие числа
        $l = (float)$m[1]; // левый операнд
        $r = (float)$m[3]; // правый операнд

        // Проверяем, какой оператор попался: умножение или деление
        if ($m[2] == '*') {
            $res = $l * $r;   // выполняем умножение
        } else {
            // Если деление на ноль — возвращаем специальное значение NAN
            if ($r == 0) return NAN;
            $res = $l / $r;   // выполняем деление
        }

        // Заменяем найденное выражение на результат 
        // И снова вызываем evaluate, потому что могли остаться другие операции
        return evaluate(str_replace($m[0], $res, $expr));
    }

    // Ищем операции сложения и вычитания
    if (preg_match('/([+\-]?[0-9.]+)([+\-])([+\-]?[0-9.]+)/', $expr, $m)) {

        $l = (float)$m[1];
        $r = (float)$m[3];

        // Плюс или минус
        if ($m[2] == '+') {
            $res = $l + $r;
        } else {
            $res = $l - $r;
        }

        // Снова заменяем и рекурсивно вычисляем дальше
        return evaluate(str_replace($m[0], $res, $expr));
    }

    // Если ничего из вышеперечисленного не подошло — считаем, что осталось просто число
    return (float)$expr;
}

// Функция проверки корректности введённого выражения
function validate($expr) {

    // Строка со всеми разрешёнными символами
    $allowed = '0123456789+-*/().';

    // Проходим по каждому символу строки
    for ($i = 0; $i < strlen($expr); $i++) {
        $ch = $expr[$i]; // текущий символ

        // Если этого символа нет в строке разрешённых — выражение неверное
        if (strpos($allowed, $ch) === false) {
            return false;
        }
    }

    // Проверяем правильность расстановки скобок
    $balance = 0; // счётчик: открывающая скобка +1, закрывающая -1

    for ($i = 0; $i < strlen($expr); $i++) {
        if ($expr[$i] == '(') $balance++;
        if ($expr[$i] == ')') $balance--;

        // Если закрывающая скобка появилась раньше, чем открывающая — ошибка
        if ($balance < 0) return false;
    }

    // В конце баланс должен быть строго 0 (каждой открытой соответствует закрытая)
    if ($balance != 0) return false;

    // Запрещаем подряд два оператора (например "++", "--", "**", "//")
    if (strpos($expr, '++') !== false) return false;
    if (strpos($expr, '--') !== false) return false;
    if (strpos($expr, '**') !== false) return false;
    if (strpos($expr, '//') !== false) return false;

    // Если все проверки пройдены — выражение корректно
    return true;
}

// Проверяем, пришёл ли POST-запрос с параметром expr
if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset($_POST['expr'])) {

    // Сохраняем выражение из запроса в переменную
    $expression = $_POST['expr'];

    // Если выражение не прошло проверку — выводим сообщение об ошибке
    if (!validate($expression)) {
        echo "Ошибка";
        exit; // останавливаем дальнейшее выполнение
    }

    // Вычисляем выражение с помощью нашей рекурсивной функции
    $result = evaluate($expression);

    // Если результат — не число (например, деление на ноль)
    if (is_nan($result)) {
        echo "Ошибка";
    } else {
        // Иначе округляем до 10 знаков и выводим
        echo round($result, 10);
    }
    exit;
}

?>